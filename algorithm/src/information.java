class information {
    private int num;
    private int index;
    information() {
        num = 0;
        index = 0;


    }

    int getNum() {
        return num;
    }

    int getIndex() {
        return index;
    }

    void setNum(int num) {
        this.num = num;
    }

    void setIndex(int index) {
        this.index = index;
    }
}
